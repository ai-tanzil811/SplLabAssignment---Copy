#include <stdio.h>
int main()
{
    float Input1, Input2, floatMultiplication;
    scanf("%f%f", &Input1, &Input2);
    floatMultiplication = Input1 * Input2;
    printf("%.2f\n", floatMultiplication);
    return 0;
}